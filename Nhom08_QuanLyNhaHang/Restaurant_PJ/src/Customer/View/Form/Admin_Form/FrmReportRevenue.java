    private String changeStringMoney(String money) {
        StringBuilder sb = new StringBuilder(money);
        int count = 1;
        for (int i = sb.length() - 1; i >= 0; --i) {
            if (count == 3) {
                sb.insert(i, ",");
                count = 1;
            } else {
                count++;
            }
        }
        if(sb.toString().charAt(0) == ',')
            sb = new StringBuilder(sb.toString().substring(1));
        return sb.toString();
    }

    private void formWindowOpened(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowOpened
        // TODO add your handling code here:
        SimpleDateFormat sdf = new SimpleDateFormat("dd-MM-yyyy");
        lblDate.setText(lblDate.getText() + sdf.format(new Date()));

        // tổng doanh thu, chi phí, lợi nhuận tháng này
        double revenueMonth = AdminBL.totalBill("Tháng này");
        double expensesMonth = AdminBL.totalImportBill("Tháng này");
        double profitMonth = revenueMonth - expensesMonth;

        // tổng doanh thu, chi phí, lợi nhuận tháng trước
        double revenueLastMonth = AdminBL.totalBillLastMonth();
        double expensesLastMonth = AdminBL.totalImportBillLastMonth();
        double profitLastMonth = revenueLastMonth - expensesLastMonth;

        lblRevenue.setText(changeStringMoney(String.valueOf((int)revenueMonth)) + "đ");
        lblExpenses.setText(changeStringMoney(String.valueOf((int)expensesMonth)) + "đ");
        lblProfit.setText(changeStringMoney(String.valueOf((int)profitMonth)) + "đ");

        if (revenueMonth - revenueLastMonth >= 0) {
            lblPercentRevenue.setText(String.format("Tăng %.0f%% so với tháng trước", ((revenueMonth - revenueLastMonth) * 100) / revenueMonth));
        } else {
            lblPercentRevenue.setText(String.format("Giảm %.0f%% so với tháng trước", (((revenueMonth - revenueLastMonth) * -1) * 100) / revenueMonth));
        }

        if (expensesMonth - expensesLastMonth >= 0) {
            lblPercentExpenses.setText(String.format("Tăng %.0f%% so với tháng trước", ((expensesMonth - expensesLastMonth) * 100) / expensesMonth));
        } else {
            lblPercentExpenses.setText(String.format("Giảm %.0f%% so với tháng trước", (((expensesMonth - expensesLastMonth) * -1) * 100) / expensesMonth));
        }

        if (profitMonth - profitLastMonth >= 0) {
            lblPercentProfit.setText(String.format("Tăng %.0f%% so với tháng trước", ((profitMonth - profitLastMonth) * 100) / profitMonth));
        } else {
            lblPercentProfit.setText(String.format("Giảm %.0f%% so với tháng trước", (((profitMonth - profitLastMonth) * -1) * 100) / profitMonth));
        }

    }//GEN-LAST:event_formWindowOpened

