    private void setData() {
        ArrayList<ModelChart> lst = AdminBL.getListModelChart();
        // add Data to chart
        for (int i = 0; i < lst.size(); i++) {
            ModelChart mc = lst.get(i);
            chart.addData(new Customer.View.raven.chart.ModelChart(mc.getMonth(), mc.getValues()));
        }
        // show chart
        chart.start();
    }

